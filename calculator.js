class calculator {
    multiplication(num1,num2){
        return num1 * num2;;
    }
    division(num1,num2){
        return num1 / num2;
    }
    subtraction(num1,num2){
        return num1 - num2;
    }
    addition(num1,num2){
        return num1 + num2;
    }
    power(num1,num2){
        return Math.pow(num1,num2);
    }
    modulo(num1,num2){
        num1 % num2;
    }
}